use crate::util::*;
use std::collections::BTreeMap;

type Plot = Vec2u;

struct RegionMap(BTreeMap<u8, Region>);

impl RegionMap {
	fn new(input: &str) -> Self {
		let plot_map = ProxyMap::from(input).convert(|string| string.bytes().collect::<Vec<_>>());
		let mut region_map = BTreeMap::<u8, Region>::new();

		for (i, plot) in plot_map.values.iter().enumerate() {
			region_map
				.entry(*plot)
				.or_default()
				.0
				.push(plot_map.get_pos(i).unwrap());
		}

		Self(region_map)
	}

	fn total_price(&self, discount: bool) -> usize {
		self.0
			.values()
			.map(|region| region.total_price(discount))
			.sum()
	}
}

#[derive(Debug, Default)]
struct Region(Vec<Plot>);

impl Region {
	fn get_sub_regions(&self) -> Vec<Self> {
		let mut all_plots = self.0.clone();
		let mut sub_regions = vec![];

		while let Some(start_plot) = all_plots.pop() {
			let mut sub_plots: Vec<Plot> = vec![start_plot];
			let mut perimetric_plots: Vec<Plot> = vec![start_plot];

			while let Some(perimetric_plot) = perimetric_plots.pop() {
				let neighbors = self.neighbors(&perimetric_plot);

				let new_neighbors = neighbors
					.iter()
					.filter(|neighbor| !sub_plots.contains(neighbor))
					.collect::<Vec<_>>();

				sub_plots.extend(new_neighbors.clone());
				perimetric_plots.extend(new_neighbors);
			}

			all_plots.retain(|plot| !sub_plots.contains(plot));
			sub_regions.push(Region(sub_plots));
		}

		sub_regions
	}

	fn total_price(&self, discount: bool) -> usize {
		self.get_sub_regions()
			.iter()
			.map(if discount {
				Self::discount_price
			} else {
				Self::price
			})
			.sum()
	}

	fn price(&self) -> usize {
		self.area() * self.perimeter()
	}

	fn discount_price(&self) -> usize {
		self.area() * self.sides()
	}

	fn area(&self) -> usize {
		self.0.len()
	}

	fn sides(&self) -> usize {
		todo!()
	}

	fn perimeter(&self) -> usize {
		self.0
			.iter()
			.map(|plot| 4 - self.neighbors(plot).len())
			.sum()
	}

	fn perimetric_plots(&self) -> Vec<Plot> {
		self.0
			.iter()
			.filter(|plot| self.neighbors(plot).len() < 4)
			.copied()
			.collect()
	}

	fn neighbors(&self, plot: &Plot) -> Vec<Plot> {
		Vec2::cardinal()
			.iter()
			.filter_map(|dir| (plot.sign() + *dir).unsign())
			.filter(|pos| self.0.contains(pos))
			.collect()
	}
}

